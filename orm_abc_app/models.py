"""
Модели приложения orm_abc_app.

Модель AbcModel представляет учебную задачу: проверку равенства C = A + B.
Используется для демонстрации возможностей Django ORM.

Документация по моделям: https://docs.djangoproject.com/en/5.2/topics/db/models/
Справочник по полям:      https://docs.djangoproject.com/en/5.2/ref/models/fields/
Справочник по Meta:       https://docs.djangoproject.com/en/5.2/ref/models/options/

После изменений модели нужно создать и применить миграции:
    python manage.py makemigrations
    python manage.py migrate
"""

from django.db import models


# ---------------------------------------------------------------------------
# Константы и вспомогательные данные
# ---------------------------------------------------------------------------

# Список допустимых значений для поля C (choices).
# Каждый элемент — кортеж (значение_в_БД, отображаемый_текст).
# Django использует это и в формах (выпадающий список),
# и в административном интерфейсе.
C_CHOICES = (
    (0, "ноль"),
    (10, "десять"),
    (15, "пятнадцать"),
    (20, "двадцать"),
)


# ---------------------------------------------------------------------------
# Модели
# ---------------------------------------------------------------------------

class AbcModel(models.Model):
    """
    Модель для хранения параметров задачи «равна ли C сумме A и B».

    Поля:
        task         — формулировка задачи (текст вопроса)
        a            — целочисленное значение A
        b            — целочисленное значение B
        c            — целочисленное значение C (из допустимого набора)
        result       — результат проверки C == A + B (заполняется в views.py)
        current_date — дата и время последнего сохранения (auto_now)

    Примеры использования в shell (python manage.py shell):
        >>> from orm_abc_app.models import AbcModel
        >>> obj = AbcModel.objects.create(task='Тест', a=5, b=5, c=10)
        >>> AbcModel.objects.filter(a__gte=5).count()
        1
    """

    # Формулировка задачи. CharField — строка фиксированной максимальной длины.
    # verbose_name — человекочитаемое название поля (используется в формах и admin)
    task = models.CharField(
        verbose_name="Формулировка задачи",
        default="Равна ли С сумме A и B?",
        max_length=255,
    )

    # Целое число A. IntegerField хранит числа в диапазоне [-2^31, 2^31-1].
    a = models.IntegerField(
        verbose_name="Значение А",
        default=0,
    )

    # Целое число B. help_text — подсказка, отображаемая под полем в форме.
    b = models.IntegerField(
        verbose_name="Значение B",
        default=2,
        help_text="Подсказка для значения B",
    )

    # Целое число C. choices ограничивает допустимые значения (валидация).
    # В форме отображается как <select> с текстовыми подписями из C_CHOICES.
    c = models.IntegerField(
        verbose_name="Значение С",
        choices=C_CHOICES,
        default=10,
    )

    # Результат вычисления (заполняется автоматически в представлении abc_result).
    result = models.CharField(
        verbose_name="Результат",
        default="Результат не определён",
        max_length=255,
    )

    # Дата и время последнего сохранения.
    # auto_now=True — поле автоматически обновляется при каждом вызове .save().
    # Аналог: auto_now_add=True обновляется только при создании объекта.
    current_date = models.DateTimeField(
        verbose_name="Дата изменения",
        auto_now=True,
    )

    def __str__(self):
        """
        Строковое представление объекта.

        Используется в административном интерфейсе и при выводе QuerySet.
        Формат: «ID: <id>; Задача: <task>»
        """
        return f"ID: {self.id}; Задача: {self.task}"

    class Meta:
        """Метаданные модели."""

        # Человекочитаемое имя модели (единственное число) — для admin и форм
        verbose_name = "A_B_C Таблица"

        # Человекочитаемое имя модели (множественное число)
        verbose_name_plural = "A_B_C Таблицы"

        # Сортировка по умолчанию: новые записи отображаются первыми.
        # Знак «-» означает убывающий порядок.
        ordering = ("-pk",)
